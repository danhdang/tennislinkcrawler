public class ParsedTournament {

    private String tournamentStartDate;
    private String tournamentName;
    private String tournamentId;
    private Integer tournamentLevel;
    private String tournamentGoLinkId;

    public String getTournamentStartDate() {
        return tournamentStartDate;
    }

    public void setTournamentStartDate(String tournamentStartDate) {
        this.tournamentStartDate = tournamentStartDate;
    }

    public String getTournamentName() {
        return tournamentName;
    }

    public void setTournamentName(String tournamentName) {
        this.tournamentName = tournamentName;
    }

    public String getTournamentId() {
        return tournamentId;
    }

    public void setTournamentId(String tournamentId) {
        this.tournamentId = tournamentId;
    }

    public Integer getTournamentLevel() {
        return tournamentLevel;
    }

    public void setTournamentLevel(Integer tournamentLevel) {
        this.tournamentLevel = tournamentLevel;
    }

    public String getTournamentGoLinkId() {
        return tournamentGoLinkId;
    }

    public void setTournamentGoLinkId(String tournamentGoLinkId) {
        this.tournamentGoLinkId = tournamentGoLinkId;
    }
}
